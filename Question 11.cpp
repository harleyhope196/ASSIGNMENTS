#include <iostream>
using namespace std;

int main()
{
    int x = 5;

    cout << "Initial value of x = " << x << endl;

    x++;  // Increment operator
    cout << "After increment (x++) = " << x << endl;

    x--;  // Decrement operator
    cout << "After decrement (x--) = " << x << endl;

    return 0;
}
